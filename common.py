class Conv(nn.Module):
    # Standard convolution
    def __init__(self, c1, c2, k=1, s=1, p=None, g=1, act=True):  # ch_in, ch_out, kernel, stride, padding, groups
        super().__init__()
        self.conv = ACBlock(c1, c2, k, s, autopad(k, p), groups=g)    #nn.Conv2d(c1, c2, k, s, autopad(k, p), groups=g, bias=False) ,ACBlock(c1, c2, k, s, autopad(k, p), groups=g)
