class ChannelAttention(nn.Module):
    def __init__(self, in_planes, ratio=16):
        super(ChannelAttention, self).__init__()
        self.avg_pool = nn.AdaptiveAvgPool2d(1)
        self.max_pool = nn.AdaptiveMaxPool2d(1)

        self.fc1 = nn.Conv2d(in_planes, in_planes // ratio, 1, bias=False)       #nn.Conv2d(in_planes, in_planes // ratio, 1, bias=False),ACBlock(in_planes, in_planes // ratio, 1)
        #self.relu1 = nn.ReLU()
        #self.relu1 = APReLU(in_planes // ratio) #自适应ReLU
        self.relu1 = AconC(in_planes // ratio)
        #self.relu1 = DyReLUA(in_planes // ratio)
        self.fc2 = nn.Conv2d(in_planes // ratio, in_planes, 1, bias=False)       #nn.Conv2d(in_planes // ratio, in_planes, 1, bias=False),ACBlock(in_planes // ratio, in_planes, 1)

        self.sigmoid = nn.Sigmoid()

    def forward(self, x):
        b, c, _, _ = x.size()
        avg_out = self.fc2(self.relu1(self.fc1(self.avg_pool(x))))
        max_out = self.fc2(self.relu1(self.fc1(self.max_pool(x))))
        out = (avg_out + max_out).view(b, c, 1, 1)
        return x * self.sigmoid(out)


class SpatialAttention(nn.Module):
    def __init__(self, kernel_size=7):
        super(SpatialAttention, self).__init__()

        assert kernel_size in (3, 7), 'kernel size must be 3 or 7'
        padding = 3 if kernel_size == 7 else 1

        #self.conv1 = ACBlock(2, 1, kernel_size, padding=padding) #nn.Conv2d(2, 1, kernel_size, padding=padding, bias=False)  #nn.Conv2d(2, 1, kernel_size, padding=padding, bias=False)
        self.conv1 = nn.Conv2d(2, 1, kernel_size, padding=padding, bias=False)
        self.sigmoid = nn.Sigmoid()

    def forward(self, x):
        b, c, w, h = x.size()
        avg_out = torch.mean(x, dim=1, keepdim=True)
        max_out, _ = torch.max(x, dim=1, keepdim=True)
        out = torch.cat([avg_out, max_out], dim=1)
        out = self.conv1(out).view(b, 1, w, h)
        return x * self.sigmoid(x)


